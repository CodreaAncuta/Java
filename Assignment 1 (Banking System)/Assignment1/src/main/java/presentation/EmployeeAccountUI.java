	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
		
		date = new Date();
		String accountNo = jTextField1.getText();
		String type = jTextField2.getText();
		Double balance = Double.parseDouble(jTextField3.getText());
		String creationDate = jTextField4.getText();
		String ownerSsn = jTextField5.getText();

		Account acc = new Account(accountNo, balance, ownerSsn, type, creationDate);
		AccountBLL aBLL = new AccountBLL();
		int addaccount = aBLL.add(acc);
		JOptionPane.showMessageDialog(null, "The account with account number " + accountNo + " has been created!");
		
		CreateFile output = new CreateFile();
		output.appendToFileAndClose(dateFormat.format(date) + " CREATE ACCOUNT\n");
	}

