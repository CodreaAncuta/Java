	// VIEW INFO
	private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {
		date = new Date();
		String accountNo = jTextField1.getText();

		AccountBLL aBLL = new AccountBLL();
		Account acc = aBLL.viewInfoAccount(accountNo);

		jTextField2.setText(String.valueOf(acc.getType()));
		jTextField3.setText(String.valueOf(acc.getBalance()));
		jTextField4.setText(String.valueOf(acc.getCreationDate()));
		jTextField5.setText(String.valueOf(acc.getOwnerSSN()));
		CreateFile output = new CreateFile();
		output.appendToFileAndClose(dateFormat.format(date) + " VIEW INFO ACCOUNT\n");
	}

